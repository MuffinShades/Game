package game;

public class TrigPlus {
	
	//Just some more trig functions
	
	public static double sec(double x) {
		return 1 / Math.cos(x);
	}
	
	public static double cot(double x) {
		return 1 / Math.tan(x);
	}
	
	public static double csc(double x) {
		return 1 / Math.sin(x);
	}
}
